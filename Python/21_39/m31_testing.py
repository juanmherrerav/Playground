def multiply(a, b):
    """ Multiply two numbers 
    Args:
        a (int): first number
        b (int): second number
    
    Returns:
            int: the product of a and b

    >>> multiply(3, 4)
    12
    >>> multiply(5, 5)
    5
    """
    return a * b


