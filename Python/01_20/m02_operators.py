# Suma              : +
# Resta             : -
# Multiplicacion    : *
# Division          : /
# Division Entera   : //
# Exponenciación    : **
# Resto División    : %
# Negativo          : - 
print("{} results in {}".format("1+6",1+6))
print("{} results in {}".format("2*5",2*5))
print("{} results in {}".format("8/2",8/2))
print("{} results in {}".format("-3",-3))
print("{} results in {}".format("5**3",5**3))
print("{} results in {}".format("5//2",5//2))
print("{} results in {}".format("5%2",5%2))
# Menor que         : <
# Mayor que         : >
# igual que         : ==
# distinto que      : !=
# Menor o igual que : <=
# Mayor o igual que : <=
print("{} results in {}".format("1<3",1<3))
print("{} results in {}".format("1>5",1>5))
print("{} results in {}".format("1>=9",1>=9))
print("{} results in {}".format("1<=0",1<=0))
print("{} results in {}".format("1!=3",1!=3))
print("{} results in {}".format("5==6",5==6))
print("{} results in {}".format("6==4",6==4))
print("{} results in {}".format("6==6",6==6))
print("{} results in {}".format("1>3 and 1>5",1>3 and 1>5))
print("{} results in {}".format("2==3 or 5<9",2==3 or 5<9))
